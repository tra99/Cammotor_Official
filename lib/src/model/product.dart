class Product {
  final String name;
  final String img;
  final int instock;
  final int qty;
  final double discount;
  final int yearID;
  final int modelID;
  final double price;
  final int resourceID;
  final int subcategoryID;

  Product({
    required this.name,
    required this.img,
    required this.instock,
    required this.qty,
    required this.discount,
    required this.yearID,
    required this.modelID,
    required this.price,
    required this.resourceID,
    required this.subcategoryID,
  });
}
